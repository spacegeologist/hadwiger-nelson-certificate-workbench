#!/usr/bin/env python3
"""Exact (symbolic) unit-distance verification of a DIMACS edge graph + .vtx file.

Unlike scripts/verify_embedding.py (floating point with a tolerance), this proves
each edge has squared length identically equal to 1 in the exact algebraic number
field generated by the coordinates.
"""

from __future__ import annotations

import argparse
import sys
import time
from pathlib import Path

PROJECT_ROOT = Path(__file__).resolve().parents[1]
if str(PROJECT_ROOT) not in sys.path:
    sys.path.insert(0, str(PROJECT_ROOT))

from hnp.dimacs import graph_from_dimacs_edge_text
from hnp.exact_embedding import exact_non_unit_edges, parse_vtx_exact


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--edge", type=Path, required=True)
    parser.add_argument("--vtx", type=Path, required=True)
    parser.add_argument("--max-errors", type=int, default=10)
    args = parser.parse_args()

    graph = graph_from_dimacs_edge_text(args.edge.read_text(encoding="utf-8"))
    coordinates = parse_vtx_exact(args.vtx.read_text(encoding="utf-8"))

    start = time.perf_counter()
    bad = exact_non_unit_edges(graph, coordinates)
    elapsed = time.perf_counter() - start

    print(f"vertices: {len(graph.vertices)}")
    print(f"edges: {len(graph.edges)}")
    print(f"coordinates: {len(coordinates)}")
    print(f"method: exact symbolic (sympy)")
    print(f"seconds: {elapsed:.3f}")

    if bad:
        print("result: NON-UNIT EDGES FOUND (exact)")
        for a, b in bad[: args.max_errors]:
            print(f"  {a} {b}")
        if len(bad) > args.max_errors:
            print(f"  ... {len(bad) - args.max_errors} more")
        return 2

    print("result: all edges are exactly unit distance")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
